var number =  Math.floor(Math.random() * 3)

switch(number){
    case 0:
        console.log("I am 0");
        break;
    case 1:
        console.log("I am 1");
        break;
    case 2:
        console.log("I am 2");
        break;
    case 3:
        console.log("I am 3");
        break;
    default:
        console.log("I'm not a number :(")                
}